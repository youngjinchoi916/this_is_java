package chapter05_ReferenceType.sec06_Array.part01_basic;

public class my01_Declaration {

	/*
	 * 배열 : 같은 타입의 많은 양의 데이터를 다룰 수 있다.
	 * 	 - 같은 타입의 데이터를 연속된 공간에 나열시키고, 각 데이터에 인ㄷ게스(index)를 부여해 놓은 자료구조이다.
	 */
	public static void main(String[] args) {
		
		/* 배열 선언 방법 [1] */
		//타입[] 변수 -> 권장
		int[] intArr1;
		double[] doubleArr1;
		String[] strArr1;
		
		//타입 변수[]
		int intArr2[];
		double doubleArr2[];
		String strArr2[];
		
		
		/* 배열 선언 방법 [2] : 배열 선언 후 null로 초기화 */
		int[] intArr3 = null;
		
	}

}
